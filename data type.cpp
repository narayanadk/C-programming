#include<stdio.h>
void main()
{
	int a=30;
	float b=3.325;
	char c='d';
	printf("the integer value is %d\n",a);
	printf("the float value is %f\n",b);
	printf("the character value is %c",c);
	int e=2+4;
	float v=2/3;
	int sub=45-22;
	int mul=12*22;
	printf("the sum is %d\n",e);
	printf("the product of two numbers is %d\n",mul);
	printf("the difference is %d\n",sub);
	printf("the quotient is %.4f",v);
	return 0;
}
